const defaultChar = [
    "   ",
    "   ",
    " ? ",
    "   ",
    "   "
];

export const generateAscii = (text: string): string => {
    const upperText = text.toUpperCase();
    const height = 5;
    const result: string[] = Array(height).fill("");

    // Add 1-space padding between letters
    for (let i = 0; i < upperText.length; i++) {
        const char = upperText[i];
        const charArt = font[char] || defaultChar;

        for (let j = 0; j < height; j++) {
            // Ensure the line exists or default to empty string
            const line = charArt[j] || " ".repeat(charArt[0].length);
            result[j] += line + " ";
        }
    }

    return result.join("\n");
};
